package com.alibaba.fastjson.deserializer.issues3796.bean;


public class CommonObject {

	private int a;

	private int b;


	private int c;

	public CommonObject() {
	}

	public CommonObject(int a, int b) {
		this.a = a;
		this.b = b;
	}

	public int getA() {
		return a;
	}

	public void setA(int a) {
		this.a = a;
	}

	public int getB() {
		return b;
	}

	public void setB(int b) {
		this.b = b;
	}

	public int getC() {
		return c;
	}

	public void setC(int c) {
		this.c = c;
	}
}
